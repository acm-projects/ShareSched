class Course {
  final String name;
  final String location;
  final int day;
  final int minute;
  final int hour;
  final int minuteDuration;
  final int daysDuration;

  Course({
    required this.name,
    required this.location,
    required this.day,
    required this.minute,
    required this.hour,
    required this.minuteDuration,
    required this.daysDuration,
  });
}
